package Dec06;

public class SwitchCase {
    public static void main(String[] args) {
        int a = 10;

        switch (a){
            case 10:
                System.out.println("num is 10");
                break;
            case 20:
                System.out.println("num is 20");
                break;
            default:
                System.out.println("cant say");

        }
    }
}
